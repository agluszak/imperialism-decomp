#!/usr/bin/env python3
"""Promote selected functions from src/ghidra_autogen into a manual source file.

This keeps edits out of autogenerated files that are overwritten on export.
"""

from __future__ import annotations

import argparse
import re
from pathlib import Path

from tools.common.repo import repo_root_from_file, resolve_repo_path
from tools.workflow.function_ownership import (
    DEFAULT_FUNCTION_OWNERSHIP_CSV,
    FunctionOwnership,
    load_function_ownership,
    normalize_repo_relative_path,
    parse_hex_address,
    write_function_ownership,
)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--address",
        action="append",
        default=[],
        help="Function address to promote (hex, with or without 0x). Repeat for multiple.",
    )
    parser.add_argument(
        "--range",
        action="append",
        default=[],
        help=(
            "Inclusive address range to promote, format START:END "
            "(hex, with or without 0x). Repeat for multiple ranges."
        ),
    )
    parser.add_argument("--target-cpp", required=True, help="Manual destination .cpp file.")
    parser.add_argument("--module", default="IMPERIALISM")
    parser.add_argument("--autogen-dir", default="src/ghidra_autogen")
    parser.add_argument(
        "--ownership-csv",
        default=DEFAULT_FUNCTION_OWNERSHIP_CSV,
        help="Function ownership map (address|target_cpp|ownership|note).",
    )
    parser.add_argument(
        "--ownership",
        default="manual",
        help="Ownership value to write for promoted addresses (default: manual).",
    )
    parser.add_argument(
        "--ownership-note",
        default="promoted_from_autogen",
        help="Ownership note to write for promoted addresses.",
    )
    parser.add_argument(
        "--force-ownership",
        action="store_true",
        help="Allow overwriting ownership entries mapped to another file.",
    )
    parser.add_argument(
        "--overwrite-existing",
        action="store_true",
        help=(
            "Replace already-present manual blocks at requested addresses with autogen blocks. "
            "Default keeps existing manual blocks unchanged."
        ),
    )
    return parser.parse_args()


def parse_address_range(value: str) -> tuple[int, int]:
    parts = value.split(":", 1)
    if len(parts) != 2:
        raise ValueError("range must be START:END")
    start = parse_hex_address(parts[0])
    end = parse_hex_address(parts[1])
    if start > end:
        start, end = end, start
    return start, end


def parse_explicit_addresses(explicit_addresses: list[str]) -> set[int]:
    return {parse_hex_address(value) for value in explicit_addresses}


def parse_ranges(range_specs: list[str]) -> list[tuple[int, int]]:
    return [parse_address_range(spec) for spec in range_specs]


def annotation_re(module: str) -> re.Pattern[str]:
    return re.compile(
        r"^\s*//\s*(?:(?:FUNCTION|STUB)\s*:\s*"
        + re.escape(module)
        + r"\s+|GHIDRA_FUNCTION\s+"
        + re.escape(module)
        + r"\s+)(?:0x)?([0-9a-fA-F]+)\s*$",
        re.MULTILINE,
    )


def split_blocks(text: str, module: str) -> tuple[str, list[tuple[int, str]]]:
    regex = annotation_re(module)
    matches = list(regex.finditer(text))
    if not matches:
        return text, []

    preamble = text[: matches[0].start()]
    blocks: list[tuple[int, str]] = []
    for i, match in enumerate(matches):
        start = match.start()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        addr = int(match.group(1), 16)
        block = text[start:end].rstrip() + "\n\n"
        blocks.append((addr, block))
    return preamble, blocks


def collect_autogen_blocks(autogen_dir: Path, module: str) -> dict[int, str]:
    found: dict[int, str] = {}
    for cpp_path in sorted(autogen_dir.rglob("*.cpp")):
        text = cpp_path.read_text(encoding="utf-8", errors="ignore")
        _, blocks = split_blocks(text, module)
        for addr, block in blocks:
            if addr in found:
                # Keep first occurrence for stability.
                continue
            found[addr] = block
    return found


def main() -> int:
    args = parse_args()
    repo_root = repo_root_from_file(__file__)
    explicit_addresses = parse_explicit_addresses(args.address)
    ranges = parse_ranges(args.range)
    autogen_dir = resolve_repo_path(repo_root, args.autogen_dir)
    target_cpp = resolve_repo_path(repo_root, args.target_cpp)
    ownership_csv = resolve_repo_path(repo_root, args.ownership_csv)

    if not explicit_addresses and not ranges:
        raise SystemExit("No addresses requested. Use --address and/or --range.")

    if not autogen_dir.is_dir():
        raise SystemExit(f"Missing autogen directory: {autogen_dir}")

    available = collect_autogen_blocks(autogen_dir=autogen_dir, module=args.module)
    requested: set[int] = set(explicit_addresses)
    if ranges:
        for addr in available:
            for start, end in ranges:
                if start <= addr <= end:
                    requested.add(addr)
                    break

    addresses = sorted(requested)
    if not addresses:
        raise SystemExit("No functions found for the requested address/range selection.")

    missing = [addr for addr in explicit_addresses if addr not in available]
    if missing:
        missing_fmt = ", ".join(f"0x{addr:08X}" for addr in missing)
        raise SystemExit(f"Requested address(es) not found in autogen snapshot: {missing_fmt}")

    ownership_entries = load_function_ownership(ownership_csv)
    target_cpp_rel = normalize_repo_relative_path(target_cpp, repo_root)
    ownership_conflicts: list[tuple[int, str]] = []
    for addr in addresses:
        existing = ownership_entries.get(addr)
        if existing is None:
            continue
        if existing.target_cpp == target_cpp_rel:
            continue
        if existing.ownership.lower() == "autogen":
            continue
        ownership_conflicts.append((addr, existing.target_cpp))

    if ownership_conflicts and not args.force_ownership:
        details = ", ".join(
            f"0x{addr:08X}->{owner}" for addr, owner in sorted(ownership_conflicts)
        )
        raise SystemExit(
            "Ownership conflict(s) detected. Use --force-ownership to overwrite: " + details
        )

    if target_cpp.exists():
        original = target_cpp.read_text(encoding="utf-8", errors="ignore")
        preamble, existing_blocks = split_blocks(original, args.module)
    else:
        preamble = (
            "// Manual decompilation file.\n"
            "// Use `uv run python -m tools.workflow.promote_from_autogen` to seed from autogen.\n\n"
        )
        existing_blocks = []

    merged: dict[int, str] = {addr: block for addr, block in existing_blocks}
    promoted_count = 0
    for addr in addresses:
        if addr in merged and not args.overwrite_existing:
            continue
        if addr not in merged:
            promoted_count += 1
        merged[addr] = available[addr]

    ordered_addrs = sorted(merged.keys())
    body = "".join(merged[addr] for addr in ordered_addrs)
    output = preamble.rstrip() + "\n\n" + body

    target_cpp.parent.mkdir(parents=True, exist_ok=True)
    target_cpp.write_text(output, encoding="utf-8")

    ownership_updates = 0
    for addr in addresses:
        next_entry = FunctionOwnership(
            address=addr,
            target_cpp=target_cpp_rel,
            ownership=args.ownership,
            note=args.ownership_note,
        )
        if ownership_entries.get(addr) != next_entry:
            ownership_updates += 1
        ownership_entries[addr] = next_entry
    write_function_ownership(ownership_csv, ownership_entries)

    print(f"Wrote {target_cpp}")
    print(f"Functions in file: {len(ordered_addrs)}")
    print(f"Newly promoted this run: {promoted_count}")
    print(f"Ownership updates: {ownership_updates} ({ownership_csv})")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
